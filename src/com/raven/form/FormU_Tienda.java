    //agrega lo que este en los textfild a el carro de compras
    private void btAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAgregarActionPerformed
        
        int total =0;
        
        int cantidad1 = Integer.parseInt(txfcan.getText());
        int precio1 = Integer.parseInt(txfprecio.getText());
        
        if(txfcan.getText() != " "&&txfprecio.getText() != " "){
            total = precio1 *cantidad1;
           
        }
        txCarro.setText("");
        
        totalcompra+=total; 
        txCarro.setText("");
        texto += txfpro.getText() + "\t" + txfid.getText() + "\t" + txfprecio.getText() + "\t" + total + "\n";
        
        txCarro.setText(texto);
        txfpro.setText("");
        txfid.setText("");
        txfprecio.setText("");
        txfcan.setText(""); 
        
        
    }//GEN-LAST:event_btAgregarActionPerformed
    
