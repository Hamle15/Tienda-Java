    // elimina la fila escojida en la tabla
    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        int fila= tablaProm.getSelectedRow();
        if (fila >= 0){
            modelo.removeRow(fila);
        }else{
            JOptionPane.showMessageDialog(null, "Seleccionar Fila");
        }
    }//GEN-LAST:event_eliminarActionPerformed
    //elimina toda la informacion de la tabla
    private void mbElimiarTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mbElimiarTodoActionPerformed
        int fila= tablaProm.getRowCount();
        for(int i = fila-1; i >=0; i--){
            modelo.removeRow(i);
        }
    }//GEN-LAST:event_mbElimiarTodoActionPerformed

