   //guarda la informacion de la tabla
    private void guardaTabla(){
        String filePath = "src/Archivos/DatosTablaProve.txt";
        File file = new File(filePath);
        try {
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
            
            for(int i = 0; i < tablaProm.getRowCount(); i++){//rows
                for(int j = 0; j < tablaProm.getColumnCount(); j++){//columns
                    bw.write(tablaProm.getValueAt(i, j).toString()+",");
                }
                bw.newLine();
            }
            
            bw.close();
            fw.close();
            
        } catch (IOException ex) {
            Logger.getLogger(Form_TablaPro.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

